#! /bin/sh
	for x;do
	echo " _______                              
 \      \   ____ ___.__. ____   ______
 /   |   \ /  _ <   |  |/ __ \ /  ___/
/    |    (  <_> )___  \  ___/ \___ \ 
\____|__  /\____// ____|\___  >____  >
        \/       \/         \/     \/ 
"
	echo "===================================================================================================================================================="

	echo "ping"
	ping $x
	
	echo "===================================================================================================================================================="
	
	echo "Name Server"
	dig $x NS +short
	
	echo "===================================================================================================================================================="
	
	echo "Mail Server"
	dig $x MX +short
	
	echo "===================================================================================================================================================="
	
	echo "whois"
	whois $x
	
	echo "===================================================================================================================================================="
	
	echo "AS Number"
	whois -h whois.cymru.com $x

	echo "===================================================================================================================================================="

	echo "host"
	host $x
done
